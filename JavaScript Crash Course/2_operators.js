
/* 
    Operators & Equality
*/
const a=5;
const b=10;

console.log(`a : ${a} , b = ${b}`);
console.log(`a > b : ${a > b}`);
console.log(`a <= b : ${a <= b}`);

//  Strict Equality
//  compares values and data types if both are equal then it is true
console.log(`a === b : ${a === b}`);

//  Strict Inequality
console.log(`a !== b : ${a !== b}`);

//  Loose Equality
//  only cares about values, doesnt care about data types
console.log(`"5" == 5 : ${"5" == 5}`);
console.log(`"5" === 5 : ${"5" === 5}`);

//  The good operators : === , !==
//  The bad operators : == , !==
console.log("The good operators : === , !==");
console.log("The bad operators : == , !=  ");

//  Logical Operators
console.log(`true && false : ${true && false}`);
console.log(`true || false : ${true || false}`);
console.log(`!false : ${!false}`);
